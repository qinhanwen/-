const departmentsMap = departments.reduce((departmentsMap, departments) => {
  if (departmentsMap[departments.pid]) {
    departmentsMap[departments.pid].push(departments);
  } else {
    departmentsMap[departments.pid] = [departments];
  }
  return departmentsMap;
}, {});

function BFS(departments) {
  while (departments.length) {
    const department = departments.pop();
    if (departmentsMap[department.id]) {
      departments = departments.concat(departmentsMap[department.id]);
      department.children = departmentsMap[department.id];
      delete departmentsMap[department.id];
    }

    if (staffsDeptMap[department.id]) {
      departments = departments.concat(staffsDeptMap[department.id]);
      department.children = staffsDeptMap[department.id];
      delete staffsDeptMap[department.id];
    }
  }
}

